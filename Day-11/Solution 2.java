class Solution {
    int maxProduct(int[] arr) { 
        int n = arr.length;
        int max = arr[0];
        
        int prefix = 1;
        int suffix = 1;
        
        for(int i=0; i<n; i++){
            
            if(prefix == 0){
                prefix= 1;
            }
            
            if(suffix == 0){
                suffix = 1;
            }
            
            prefix = prefix * arr[i];
            suffix = suffix * arr[n-i-1];
            
            max =  Math.max(max, Math.max(prefix, suffix));
        }
        return max;
    }
}
